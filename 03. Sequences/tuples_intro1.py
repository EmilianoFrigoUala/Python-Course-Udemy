t1 = 'a', \
     'b', \
     'c',
print(t1)  # it will put the parenthesis automatically.
# note that it's similar to lists, but with parenthesis instead.

# we can create tuples putting parenthesis as well.
# as a general rule, we should always use parenthesis just to avoid
# remembering when they are needed or not.
t2 = ('a',
      'b',
      'c',
      )
print(t2)